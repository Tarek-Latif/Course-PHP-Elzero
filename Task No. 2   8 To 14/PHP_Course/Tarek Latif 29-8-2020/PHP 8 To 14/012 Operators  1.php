<?php
/*
Arithmetic Operators
[ + ] Addition
*/
$var1 = 1000;
$var2 = 2000;
$var3 = 3000;
echo  $var1 + $var2 + $var3  ;  // 1000+2000+3000
echo "<br/>";

/*
Arithmetic Operators
[ - ] Subtraction
*/
$var1 = 10000;
$var2 = 2000;
$var3 = 3000;
echo  $var1 - $var2 - $var3  ;  // 10000-2000-3000
echo "<br/>";

/*
Arithmetic Operators
[ * ] Multiplication
*/
$var1 = 100;
$var2 = 200;
echo  $var1 * $var2  ;  // 100-200
echo "<br/>";

/*
Arithmetic Operators
[ / ] Division
*/
$var1 = 100;
$var2 = 20;
echo  $var1 / $var2  ;  // 100/20
echo "<br/>";

/*
Arithmetic Operators
[ % ] Modulus
*/
$var1 = 119;
$var2 = 20;
echo  $var1 % $var2  ;  // 119 % 20
echo "<br/>";

/*
Arithmetic Operators
[ ** ] Expontion
*/
$var1 = 2;
$var2 = 10;
echo  $var1 ** $var2  ;  // 2 ** 10

