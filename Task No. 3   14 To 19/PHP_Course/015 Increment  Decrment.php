<?php
/*
Increment / Decrement Operators

[ ++ $var] pre Increment
[ $var++]  Post Increment
*/
echo "<h2> Increment Operators </h2>";
$var = 10;
echo ++$var . "<br>" ;
echo ++$var . "<br>" ;
echo ++$var . "<br>" ;
echo ++$var . "<br>" ;
echo "<br>";

echo "<h2> Increment Operators </h2>";
$var2 = 20;
echo $var2++ . "<br>";
echo $var2++ . "<br>";
echo $var2++ . "<br>";
echo $var2++ . "<br>";

echo "<br>";

echo "<h2> Decrement Operators </h2>";

$var = 10;
echo --$var . "<br>" ;
echo --$var . "<br>" ;
echo --$var . "<br>" ;
echo --$var . "<br>" ;

echo "<br>";

 echo "<h2> Decrement Operators </h2>";

$var = 20;
echo $var-- . "<br>" ;
echo $var-- . "<br>" ;
echo $var-- . "<br>" ;
echo $var-- . "<br>" ;